/*
  *Var
    -> Espaço alocado na memória que guarda dados

  *Tipos:
    -> var;
    -> let;
    -> const;
*/

//var
var clima = "quente"
console.log(clima)
clima = "frio"
console.log(clima)
//let
let clima2 = "quente"
console.log(clima2)
clima2 = "frio"
console.log(clima2)
//const
const meuElo = "prata"
console.log(meuElo)
